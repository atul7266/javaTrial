public class forloop {
    public static void main(String[] args) {

        for (int i=0; i<6; i++ ){
            System.out.print("*");

        }for (int j=0; j<4; j++){
            System.out.print(" * ");

        }for (int k=0; k<2; k++){
            System.out.print(" * ");

        }

    }
}
